const convertSectionsToHTML = (sections) => {
  let html = "";

  sections.forEach((section) => {
    switch (section.type) {
      case "heading":
        html += `<h3 class="text-xl font-bold mt-6 mb-3 text-cyan-100">${section.content}</h3>`;
        break;
      case "paragraph":
        html += `<p class="mb-4 text-gray-300">${section.content}</p>`;
        break;
      case "unordered_list":
        html +=
          '<ul class="list-disc list-inside mb-4 space-y-2 text-gray-300">';
        section.content.forEach((item) => {
          html += `<li>${item}</li>`;
        });
        html += "</ul>";
        break;
      default:
        html += `<p class="mb-4 text-gray-300">${section.content}</p>`;
    }
  });

  return html;
};

